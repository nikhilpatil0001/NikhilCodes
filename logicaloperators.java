public class logicaloperators{
 public static void main(String args[]){

 System.out.println((10>5) && (60<80));// when both are true then true otherwise all false
 System.out.println((10>5) || (60<80));// when both are false then false otherwise true
 System.out.println( !(10>5) );// true = false  False = true


 }

}