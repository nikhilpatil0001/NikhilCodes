public class Datatype{

    public static void main(String args[]){

        byte b =100; // stores any random value (upto 256)
        System.out.println(b);
        float price =33.5f; //stores any small point value
        System.out.println(price);
        char ch ='x';// stores any single character ' ' ex. a,b,,f,g,t,r,r
        System.out.println(ch);
        boolean var = true;// stores any statement i.e true or false
        System.out.println(var);
        int number = 55; // stores any whole number 
        System.out.println(number);
        long i =556;// big interger value 
        System.out.println(i);
        double point = 556.3;// big float value
        System.out.println(point);
        short n =240;// stores any number
        System.out.println(n);


    }





}